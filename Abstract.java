abstract class Abs
{
//	abstract 
//	private 
//	public 
//	static 
	Abs()
	{
		System.out.println("constructor");
	}	
	void run()
	{
		System.out.println("running1");
	}
}
class Abstract extends Abs
{
	void run()
	{
		super.run();
		System.out.println("running");
	}
	public static void main(String[] args)
	{
		Abs obj=new Abstract();
		obj.run();
	}
}		
